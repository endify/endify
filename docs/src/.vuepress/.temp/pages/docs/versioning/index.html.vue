<template><h1 id="versioning" tabindex="-1"><a class="header-anchor" href="#versioning" aria-hidden="true">#</a> Versioning</h1>
<p>We use semantic versioning for all packages and use common tags for releases.</p>
<p>That means:</p>
<ul>
<li><code>@latest</code> is always the latest stable version</li>
<li><code>@next</code> is always the latest development version</li>
<li><code>vX.X.X-beta.X</code> means development build of the future version of Endify</li>
</ul>
<p>We stick to minor <code>npm</code> and <code>node.js</code> versions with Endify minor versions, same for major.</p>
<p>If you need support for older versions of <code>npm</code> and <code>Node.js</code> check our troubleshooting guide for most common problems.</p>
</template>
